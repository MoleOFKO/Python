#Input from user
print("---Input from user---")
first = int(input("Enter first number : "))
second = int(input("Enter second number : "))

#Division
print("---Division---")
div = first / second
print(first, "is divided by", second, ".", "Result is: ", div)
print("------------------------------------------------------")

#Modulus
print("---Modulus---")
mod = first % second
print(first, "is divided by", second, ".")
print("Remainder is: ", mod)
print("------------------------------------------------------")

