print("===Comparison or Relational Operators===")

print("1. Equal to ==")
print("Example: ")
print("5 == 5 is", 5 == 5) #True
print("3 == 5 is", 3 == 5) #False

print("2. Not Equal to !=")
print("Example: ")
print("3 != 3 is", 3 != 3) #False
print("3 != 4 is", 3 != 4) #True

print("3. Less than <")
result = 3 < 5 #True
result1 = 5 < 3 #False
print("3 < 5 is", result, "and 5 < 3 is", result1)

print("4. Greater than >")
result = 5 > 2 #True
result1 = 5 > 6 #False
print("5 > 2 is", result, "and 5 > 6 is", result1)

print("5. Less than or Equal to <=")
print("5 <= 5 is", 5 <= 5) #True
print("6 <= 5 is", 6 <= 5) #False


print("6. Greater than or Equal to >=")
print("5 >= 6 is", 5 >= 6) #False
