#loop through a list
#length     =   len() is uses to find the length of a list

#Example
fruits = ["Apple","Banana","Orange"]
print(fruits)
print("Number of Items: ", len(fruits))
for fruit in fruits:
    print(fruit)


#Example with range() function
devices = ["laptop", "phone", "tablet"]
print(devices)
for device in range(len(devices)):
    print(device)


#Loop through a string
for letter in "Python":
    print(letter)



